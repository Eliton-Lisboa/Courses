#include <stdio.h>
#include <stdlib.h>

void main() {
    int x = 6, y = 4;

    printf ("\nThe sum between %d and %d is equal to: %d", x, y, x + y);
    printf ("\nThe subtraction between %d and %d is equal to: %d", x, y, x - y);
    printf ("\nThe division between %d and %d is equal to: %d", x, y, x / y);
    printf ("\nThe rest of division between %d and %d is equal to: %d", x, y, x % y);
    printf ("\nThe absolute value of -3 is equal to: %d", abs(-3));
}
